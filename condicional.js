let num = parseInt(prompt("Insira um número de 1 a 5: "));

if (num == 1) {
    alert("Número um");
} else if (num == 2) {
    alert("Número dois");
} else if (num == 3) {
    alert("Número três");
} else if (num == 4) {
    alert("Número quatro");
} else if (num == 5) {
    alert("Número cinco");
} else {
    alert ("Número não informado!")
}